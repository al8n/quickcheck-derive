//! Code generation for the `arbitrary` and `shrink` method bodies.

use proc_macro2::{Span, TokenStream as TokenStream2};
use quote::quote;
use syn::{spanned::Spanned, DataEnum, DeriveInput, Error, Fields, Ident, Path};

use crate::attrs::{ContainerAttrs, FieldAttrs, VariantAttrs};

/// Allocates macro-internal identifiers that cannot collide with the target
/// type's `const` generic parameters.
///
/// `const` params bypass macro hygiene for rustc's E0530 ("let bindings cannot
/// shadow const parameters") and for pattern-binding resolution, so
/// `Span::mixed_site()` alone is insufficient — a user `const __quickcheck_chain`
/// would still clash with a generated `__quickcheck_chain` local. So whenever the
/// type has a `const` param spelled like one of our `__quickcheck_*` internals,
/// we append a trailing-underscore suffix to *every* generated name, long enough
/// that no generated name (which never ends in `_`) can equal any such param. In
/// the common case (no `__quickcheck_*`-named const param) the suffix is empty.
pub(crate) struct Hygiene {
  suffix: String,
}

impl Hygiene {
  pub(crate) fn new(input: &DeriveInput) -> Self {
    let max_trailing_underscores = input
      .generics
      .const_params()
      .map(|p| p.ident.to_string())
      .filter(|name| name.starts_with("__quickcheck_"))
      .map(|name| name.chars().rev().take_while(|&c| c == '_').count())
      .max();
    let suffix = match max_trailing_underscores {
      Some(k) => "_".repeat(k + 1),
      None => String::new(),
    };
    Self { suffix }
  }

  /// A `mixed_site` identifier for `base`, suffixed so it cannot collide with a
  /// user `const` parameter.
  pub(crate) fn ident(&self, base: &str) -> Ident {
    Ident::new(&format!("{base}{}", self.suffix), Span::mixed_site())
  }
}

/// How a single field is generated by `arbitrary`. `g` is the (hygienic)
/// `&mut Gen` parameter ident threaded from `lib.rs`.
fn field_arbitrary_expr(attrs: &FieldAttrs, g: &Ident, qc: &Path) -> TokenStream2 {
  if let Some(with) = &attrs.with {
    quote!(#with(#g))
  } else if attrs.default {
    quote!(::core::default::Default::default())
  } else {
    quote!(#qc::Arbitrary::arbitrary(#g))
  }
}

/// Whether a field participates in shrinking.
fn field_is_shrinkable(attrs: &FieldAttrs) -> bool {
  // Held constant when `default`, or `with` without an explicit `shrink`.
  if attrs.default {
    return false;
  }
  if attrs.with.is_some() && attrs.shrink.is_none() {
    return false;
  }
  true
}

/// The per-field shrink iterator expression, given how to refer to the field's
/// current value (`access`, e.g. `&self.x` or `__quickcheck_field0`).
fn field_shrink_iter(attrs: &FieldAttrs, access: &TokenStream2, qc: &Path) -> TokenStream2 {
  if let Some(shrink) = &attrs.shrink {
    quote!(#shrink(#access))
  } else {
    quote!(#qc::Arbitrary::shrink(#access))
  }
}

/// Generate the `arbitrary` + `shrink` bodies for a struct.
///
/// `g` is the (hygienic) `&mut Gen` parameter ident threaded from `lib.rs`,
/// shared by the impl signature and these bodies.
pub(crate) fn struct_bodies(
  name: &Ident,
  fields: &Fields,
  container: &ContainerAttrs,
  g: &Ident,
  hyg: &Hygiene,
  box_ty: &Path,
  qc: &Path,
) -> syn::Result<(TokenStream2, TokenStream2)> {
  let field_attrs = parse_field_attrs(fields)?;

  // --- arbitrary ---
  let arbitrary = if let Some(with) = &container.with {
    quote!(#with(#g))
  } else {
    let ctor = build_ctor(quote!(#name), fields, &field_attrs, g, qc);
    quote!(#ctor)
  };

  // --- shrink ---
  // Container `shrink` wins; else `with`-without-`shrink` => empty; else derive.
  let shrink = if let Some(shrink) = &container.shrink {
    quote!(#shrink(self))
  } else if container.with.is_some() {
    empty_iter(box_ty)
  } else {
    struct_shrink(fields, &field_attrs, hyg, box_ty, qc)
  };

  Ok((arbitrary, shrink))
}

/// Generate the `arbitrary` + `shrink` bodies for an enum.
///
/// `g` is the (hygienic) `&mut Gen` parameter ident threaded from `lib.rs`,
/// shared by the impl signature and these bodies.
pub(crate) fn enum_bodies(
  name: &Ident,
  data: &DataEnum,
  container: &ContainerAttrs,
  g: &Ident,
  hyg: &Hygiene,
  box_ty: &Path,
  qc: &Path,
) -> syn::Result<(TokenStream2, TokenStream2)> {
  // --- arbitrary ---
  let arbitrary = if let Some(with) = &container.with {
    quote!(#with(#g))
  } else {
    enum_arbitrary(name, data, g, qc)?
  };

  // --- shrink ---
  // Container `shrink` wins; else `with`-without-`shrink` => empty; else derive.
  let shrink = if let Some(shrink) = &container.shrink {
    quote!(#shrink(self))
  } else if container.with.is_some() {
    empty_iter(box_ty)
  } else {
    enum_shrink(name, data, hyg, box_ty, qc)?
  };

  Ok((arbitrary, shrink))
}

