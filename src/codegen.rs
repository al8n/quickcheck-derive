/// Generate the enum `arbitrary` body: uniformly choose a non-skipped variant.
fn enum_arbitrary(
  name: &Ident,
  data: &DataEnum,
  g: &Ident,
  qc: &Path,
) -> syn::Result<TokenStream2> {
  let mut indices = Vec::new();
  let mut arms = Vec::new();

  for variant in &data.variants {
    let vattrs = VariantAttrs::parse(&variant.attrs)?;
    if vattrs.skip {
      continue;
    }

    let arm_index = indices.len();
    indices.push(arm_index);

    let vname = &variant.ident;
    let path = quote!(#name::#vname);

    let build = if let Some(with) = &vattrs.with {
      quote!(#with(#g))
    } else {
      let fattrs = parse_field_attrs(&variant.fields)?;
      build_ctor(path, &variant.fields, &fattrs, g, qc)
    };

    let lit = syn::Index::from(arm_index);
    arms.push(quote!(#lit => #build,));
  }

  if indices.is_empty() {
    return Err(Error::new(
      data.variants.span(),
      "all enum variants are `#[quickcheck(skip)]`; `arbitrary` cannot pick a variant",
    ));
  }

  let index_lits = indices.iter().map(|i| syn::Index::from(*i));

  Ok(quote! {
    match *#g.choose(&[#(#index_lits),*]).unwrap() {
      #(#arms)*
      _ => unreachable!(),
    }
  })
}

/// Generate the enum `shrink` body: match the current variant and shrink it.
fn enum_shrink(
  name: &Ident,
  data: &DataEnum,
  hyg: &Hygiene,
  box_ty: &Path,
  qc: &Path,
) -> syn::Result<TokenStream2> {
  let empty = empty_iter(box_ty);
  let mut arms = Vec::new();

  for variant in &data.variants {
    let vattrs = VariantAttrs::parse(&variant.attrs)?;
    let vname = &variant.ident;

    // For the non-field-derived cases, use a non-binding pattern so we never
    // emit unused bindings. The field-derived path builds its own pattern that
    // binds exactly the fields it shrinks.
    let arm = if vattrs.skip {
      let pattern = variant_wildcard_pattern(name, vname, &variant.fields);
      quote!(#pattern => { #empty } , )
    } else if let Some(shrink) = &vattrs.shrink {
      let pattern = variant_wildcard_pattern(name, vname, &variant.fields);
      quote!(#pattern => { #shrink(self) } , )
    } else if vattrs.with.is_some() {
      // `with` without `shrink` ⇒ empty.
      let pattern = variant_wildcard_pattern(name, vname, &variant.fields);
      quote!(#pattern => { #empty } , )
    } else {
      let fattrs = parse_field_attrs(&variant.fields)?;
      let (pattern, body) = variant_shrink(name, vname, &variant.fields, &fattrs, hyg, box_ty, qc);
      quote!(#pattern => { #body } , )
    };

    arms.push(arm);
  }

  if arms.is_empty() {
    // Empty enums are uninhabited; nothing to shrink.
    return Ok(empty_iter(box_ty));
  }

  Ok(quote! {
    match self {
      #(#arms)*
    }
  })
}

/// A non-binding match pattern for a variant (`Variant`, `Variant(..)`,
/// `Variant { .. }`), used when the arm body does not reference fields.
fn variant_wildcard_pattern(name: &Ident, vname: &Ident, fields: &Fields) -> TokenStream2 {
  match fields {
    Fields::Named(_) => quote!(#name::#vname { .. }),
    Fields::Unnamed(_) => quote!(#name::#vname ( .. )),
    Fields::Unit => quote!(#name::#vname),
  }
}

/// Generate the `(pattern, body)` for a variant's field-derived shrink arm.
///
/// Mirrors struct shrink: clone the whole `self` as a base, then per produced
/// item clone the base and mutate just the shrunk field in place (via a
/// `match &mut out` on the variant). This depends only on `Self: Clone` — never
/// on individual fields being `Clone` — so held / `default` / `with` fields that
/// are not `Clone` are fine. Returns a non-binding pattern + empty body when
/// there is nothing to shrink.
fn variant_shrink(
  name: &Ident,
  vname: &Ident,
  fields: &Fields,
  field_attrs: &[FieldAttrs],
  hyg: &Hygiene,
  box_ty: &Path,
  qc: &Path,
) -> (TokenStream2, TokenStream2) {
  // Indices of fields that actually shrink.
  let shrinkable: Vec<usize> = field_attrs
    .iter()
    .enumerate()
    .filter(|(_, a)| field_is_shrinkable(a))
    .map(|(i, _)| i)
    .collect();

  if matches!(fields, Fields::Unit) || shrinkable.is_empty() {
    return (
      variant_wildcard_pattern(name, vname, fields),
      empty_iter(box_ty),
    );
  }

  let chain = hyg.ident("__quickcheck_chain");
  let base = hyg.ident("__quickcheck_base");
  let out = hyg.ident("__quickcheck_out");
  let v = hyg.ident("__quickcheck_v");
  let slot = hyg.ident("__quickcheck_slot");

  // One binding ident per shrinkable field (bound as `&Field` in the outer
  // pattern via default match modes), used to feed that field's shrink iterator.
  let bindings: Vec<(usize, Ident)> = shrinkable
    .iter()
    .map(|&i| (i, hyg.ident(&format!("__quickcheck_field{i}"))))
    .collect();

  let outer_pattern = variant_bind_pattern(name, vname, fields, &bindings);
  let field_count = fields.len();

  let mut clauses = Vec::new();
  for (i, binding) in &bindings {
    let iter = field_shrink_iter(&field_attrs[*i], &quote!(#binding), qc);
    let slot_pattern = variant_slot_pattern(name, vname, fields, *i, field_count, &slot);
    clauses.push(quote! {
      {
        let #base = ::core::clone::Clone::clone(self);
        #chain = #box_ty::new(#chain.chain(
          (#iter).map(move |#v| {
            let mut #out = ::core::clone::Clone::clone(&#base);
            // `out` is always this variant, but a `match` (not `if let`) keeps
            // this warning-free for single-variant enums, where an `if let`
            // would be irrefutable. The wildcard is unreachable only for
            // single-variant enums, hence the `allow`.
            match &mut #out {
              #slot_pattern => *#slot = #v,
              #[allow(unreachable_patterns)]
              _ => {}
            }
            #out
          })
        ));
      }
    });
  }

  let body = quote! {
    let mut #chain: #box_ty<dyn ::core::iter::Iterator<Item = Self>> =
      #box_ty::new(::core::iter::empty());
    #(#clauses)*
    #chain
  };

  (outer_pattern, body)
}

/// Outer match pattern binding exactly the shrinkable fields (by their internal
/// ident, as `&Field`), ignoring the rest.
fn variant_bind_pattern(
  name: &Ident,
  vname: &Ident,
  fields: &Fields,
  bindings: &[(usize, Ident)],
) -> TokenStream2 {
  match fields {
    Fields::Named(named) => {
      let binds = named.named.iter().enumerate().filter_map(|(i, f)| {
        bindings.iter().find(|(bi, _)| *bi == i).map(|(_, ident)| {
          let fname = f.ident.as_ref().expect("named field has ident");
          quote!(#fname: #ident)
        })
      });
      quote!(#name::#vname { #(#binds,)* .. })
    }
    Fields::Unnamed(unnamed) => {
      let elems =
        (0..unnamed.unnamed.len()).map(|i| match bindings.iter().find(|(bi, _)| *bi == i) {
          Some((_, ident)) => quote!(#ident),
          None => quote!(_),
        });
      quote!(#name::#vname ( #(#elems),* ))
    }
    Fields::Unit => unreachable!(),
  }
}

/// `&mut` match pattern binding only field `index` as `slot`, ignoring the rest.
fn variant_slot_pattern(
  name: &Ident,
  vname: &Ident,
  fields: &Fields,
  index: usize,
  field_count: usize,
  slot: &Ident,
) -> TokenStream2 {
  match fields {
    Fields::Named(named) => {
      let fname = named.named[index]
        .ident
        .as_ref()
        .expect("named field has ident");
      quote!(#name::#vname { #fname: #slot, .. })
    }
    Fields::Unnamed(_) => {
      let elems = (0..field_count).map(|i| if i == index { quote!(#slot) } else { quote!(_) });
      quote!(#name::#vname ( #(#elems),* ))
    }
    Fields::Unit => unreachable!(),
  }
}

/// `<box_ty>::new(empty())`.
fn empty_iter(box_ty: &Path) -> TokenStream2 {
  quote!(#box_ty::new(::core::iter::empty()))
}
