/// Parse the per-field `#[quickcheck(...)]` attributes for a field list.
fn parse_field_attrs(fields: &Fields) -> syn::Result<Vec<FieldAttrs>> {
  fields.iter().map(|f| FieldAttrs::parse(&f.attrs)).collect()
}

/// Build a constructor expression for `path` (a struct or variant path) over
/// `fields`, generating each field from `g` per its attributes.
fn build_ctor(
  path: TokenStream2,
  fields: &Fields,
  field_attrs: &[FieldAttrs],
  g: &Ident,
  qc: &Path,
) -> TokenStream2 {
  match fields {
    Fields::Named(named) => {
      let inits = named.named.iter().zip(field_attrs).map(|(f, attrs)| {
        let ident = f.ident.as_ref().expect("named field has ident");
        let expr = field_arbitrary_expr(attrs, g, qc);
        quote!(#ident: #expr)
      });
      quote!(#path { #(#inits),* })
    }
    Fields::Unnamed(unnamed) => {
      let inits = unnamed.unnamed.iter().zip(field_attrs).map(|(_, attrs)| {
        let expr = field_arbitrary_expr(attrs, g, qc);
        quote!(#expr)
      });
      quote!(#path ( #(#inits),* ))
    }
    Fields::Unit => quote!(#path),
  }
}

/// Generate the struct `shrink` body. Shrinks one field at a time, holding the
/// rest at their current value by cloning `self`.
fn struct_shrink(
  fields: &Fields,
  field_attrs: &[FieldAttrs],
  hyg: &Hygiene,
  box_ty: &Path,
  qc: &Path,
) -> TokenStream2 {
  let chain = hyg.ident("__quickcheck_chain");
  let base = hyg.ident("__quickcheck_base");
  let out = hyg.ident("__quickcheck_out");
  let v = hyg.ident("__quickcheck_v");
  let mut clauses = Vec::new();

  for (member, attrs) in members(fields).zip(field_attrs) {
    if !field_is_shrinkable(attrs) {
      continue;
    }
    let access = quote!(&self.#member);
    let iter = field_shrink_iter(attrs, &access, qc);
    clauses.push(quote! {
      {
        let #base = ::core::clone::Clone::clone(self);
        #chain = #box_ty::new(#chain.chain(
          (#iter).map(move |#v| {
            let mut #out = ::core::clone::Clone::clone(&#base);
            #out.#member = #v;
            #out
          })
        ));
      }
    });
  }

  if clauses.is_empty() {
    return empty_iter(box_ty);
  }

  quote! {
    let mut #chain: #box_ty<dyn ::core::iter::Iterator<Item = Self>> =
      #box_ty::new(::core::iter::empty());
    #(#clauses)*
    #chain
  }
}

/// Iterator of `syn::Member`s (named idents or positional indices) for a field
/// list, suitable for `self.#member` access.
fn members(fields: &Fields) -> impl Iterator<Item = syn::Member> + '_ {
  fields.iter().enumerate().map(|(i, f)| match &f.ident {
    Some(ident) => syn::Member::Named(ident.clone()),
    None => syn::Member::Unnamed(syn::Index::from(i)),
  })
}

